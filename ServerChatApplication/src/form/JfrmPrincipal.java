    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:
        txtLog.append("Conectando...\r\n");
        String resposta = JOptionPane.showInputDialog("Digite a porta que deseja:");
        try {
            Integer port = parseInt(resposta);
            serverSocket = new ServerSocket(port);
            JOptionPane.showMessageDialog(null, "Servidor conectador na porta: " +  port);
            txtLog.append("Servidor ativo na porta: " +  port + "\r\n");
            server = new ServerSide(serverSocket, txtLog);
            onDeactive();
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Digite uma porta valida!");
            txtLog.append("Porta digitada incorretamente!\r\n");
            txtLog.append("Falha ao iniciar o Servidor!!\r\n");
            txtLog.append("Tente novamente!!\r\n");
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void btnDesconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesconectarActionPerformed
        // TODO add your handling code here:
        txtLog.append("Servidor desconectado!\r\n");
        if(server != null){
            server.stop();
        }
        
        onActive();
    }//GEN-LAST:event_btnDesconectarActionPerformed

    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
        // TODO add your handling code here:
        txtLog.append("Servidor desconectado!\r\n");
        if(server != null){
            server.stop();
        }
        dispose();
    }//GEN-LAST:event_btnFecharActionPerformed

